// Add custom Jest matchers
expect.extend({
  toBeAfter(received, expected) {
    const pass = new Date(received) > new Date(expected);
    if (pass) {
      return {
        message: () => `expected ${received} not to be after ${expected}`,
        pass: true,
      };
    } else {
      return {
        message: () => `expected ${received} to be after ${expected}`,
        pass: false,
      };
    }
  },
});